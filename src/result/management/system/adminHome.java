    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String courseName = (String) jComboBox1.getSelectedItem();
        String branchName = (String) jComboBox2.getSelectedItem();
        String rollNumber = rollNumberTextField.getText();
        String name = nameTextField.getText();
        String gender = (String) jComboBox3.getSelectedItem();
        String fatherName = fatherNameTextField.getText();
        String mobileNumber = mobileNumberTextField.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentresultmanagementsystem","root","root");
            Statement st = con.createStatement();
            st.executeUpdate("insert into student(rollNumber,courseName,branchName,name,gender,fatherName,mobileNumber) values ('"+rollNumber+"','"+courseName+"','"+branchName+"','"+name+"','"+gender+"','"+fatherName+"','"+mobileNumber+"')");
            JOptionPane.showMessageDialog(this,"Data Saved Successfully");
            setVisible(false);
            new adminHome().setVisible(true);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

